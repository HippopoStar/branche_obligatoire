int		ft_sqrt(int nb)
{
	int		i;
	int		a;

	i = 0;
	a = 0;
	if (nb < 0)
	{
		return (0);
	}
	else
	{
		while (a < nb)
		{
			i++;
			a = i * i;
		}
		if (a == nb)
		{
			return (i);
		}
		else
		{
			return (0);
		}
	}
}
