#include "libft.h"

int		ft_atoi(const char *str)
{
	size_t	i;
	int		n;
	int		p_o_n;

	i = 0;
	while (*(str + i) == '\t' || *(str + i) == '\n' || *(str + i) == '\r'
			|| *(str + i) == '\v' || *(str + i) == '\f' || *(str + i) == ' ')
	{
		i++;
	}
	p_o_n = 1;
	if (*(str + i) == '+' || *(str + i) == '-')
	{
		if (*(str + i) == '-')
			p_o_n = -1;
		i++;
	}
	n = 0;
	while ('0' <= *(str + i) && *(str + i) <= '9')
	{
		n = (n * 10) + (*(str + i) - '0');
		i++;
	}
	return (p_o_n * n);
}
